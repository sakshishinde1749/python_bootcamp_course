import random

# to print random number between a and b
random_int = random.randint(1, 10)
print(random_int)

# to print float random number between 0 to 1
random_num_0_to_1 = random.random()
print(random_num_0_to_1)

# to print float random between a and b
random_float = random.uniform(1, 10)
print(random_float)